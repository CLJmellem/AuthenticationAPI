using Auth.Application.Interfaces;
using System.Security.Cryptography;
using System.Text;

namespace Auth.Infrastructure.Services;

/// <summary>
/// Encryption Service
/// </summary>
public class EncryptionService : IEncryptionService
{
    /// <summary>The key</summary>
    /// Since this is a project example, the key is hardcoded here. In a real-world application, it should be stored securely.
    public byte[] _key = Convert.FromHexString("02d11fc4ddd4d4ac5c20ac2e6d20724c");

    /// <summary>Encrypts the specified text to encrypt.</summary>
    /// <param name="textToEncrypt">The text to encrypt.</param>
    /// <returns></returns>
    public string Encrypt(string textToEncrypt)
    {
        byte[] encrypted;
        Aes aes = Aes.Create();
        aes.Key = _key;
        aes.GenerateIV();

        ICryptoTransform encryptor = aes.CreateEncryptor(aes.Key, aes.IV);

        using (MemoryStream msEncrypt = new MemoryStream())
        {
            using (CryptoStream csEncrypt = new CryptoStream(msEncrypt, encryptor, CryptoStreamMode.Write))
            {
                using (StreamWriter swEncrypt = new StreamWriter(csEncrypt))
                {
                    swEncrypt.Write(textToEncrypt);
                }
                encrypted = msEncrypt.ToArray();
            }
        }

        return Convert.ToBase64String(encrypted);
    }

    /// <summary>Decrypts the specified text to decrypt.</summary>
    /// <param name="textToDecrypt">The text to decrypt.</param>
    /// <returns></returns>
    public string Decrypt(string textToDecrypt)
    {
        string decrypted;
        Aes aes = Aes.Create();
        aes.Key = _key;
        aes.GenerateIV();

        ICryptoTransform decryptor = aes.CreateDecryptor(aes.Key, aes.IV);

        using (MemoryStream msDecrypt = new MemoryStream(Convert.FromBase64String(textToDecrypt)))
        {
            using (CryptoStream csDecrypt = new CryptoStream(msDecrypt, decryptor, CryptoStreamMode.Read))
            {
                using (StreamReader srDecrypt = new StreamReader(csDecrypt))
                {
                    decrypted = srDecrypt.ReadToEnd();
                }
            }
        }

        return decrypted;
    }
}